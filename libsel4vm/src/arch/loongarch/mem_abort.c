static int handle_fw_cfg_access(fault_t *fault)
{
    uint8_t rj, rd;
    uintptr_t addr = fault_get_address(fault);
    uint8_t wide, size;
    uint32_t inst = fault_get_inst(fault);
    
    enum mem_op_type op;
    uint8_t is_opcode_valid = false;

    /* LDPTR, STPTR */
    uint16_t opcode = (inst >> 24) & 0xff;
    if (opcode >= 0x24 && opcode <= 0x27) {
        
        if (opcode & 1) {
            op = ST;
        } else {
            op = LD;
        }

        if (op == ST) {
            if ((opcode & 0x3) == 0x1) {
                wide = 2;
            } else {
                wide = 3;
            }
        } else {
            if ((opcode & 0x3) == 0x0) {
                wide = 2;
            } else {
                wide = 3;
            }
        }

        is_opcode_valid = true; 
    }

    /* ld, st, ld.u */
    opcode = (inst >> 22) & 0x3ff;
    if (opcode >= 0xa0 && opcode <= 0xaa) {

        if (opcode >= 0xa0 && opcode <= 0xa3) {
            op = LD;
        } else if (opcode >= 0xa4 && opcode <= 0xa7) {
            op = ST;
        } else {
            op = LD;
        }

        wide = opcode & 0x3; 

        is_opcode_valid = true;
    }

    if (!is_opcode_valid) {
        return -1;
    }

    size = 1 << wide;
    assert(size > 0 && size <= sizeof(uint64_t));
    uint8_t size_saved = size; /* for debug */

    rj = (inst >> 5) & 0x1f;
    rd = inst & 0x1f;

    seL4_UserContext *regs = fault_get_ctx(fault);


    if (addr == 0x1e020008) {
        /* fw_cfg_select */
        if (op != ST) {
            return 0;
        }
        cur_offset = 0;
        uint64_t tmp = sel4utils_reg_get_val(*regs, rd);
        cur_entry = ((tmp & 0xff) << 8) | ((tmp >> 8) & 0xff);

        // printf("fw_cfg_select, inst: 0x%x, rd: 0x%x, selected_entry: 0x%x\n", inst, rd, cur_entry);

    } else if (addr == 0x1e020000) {
        /* fw_cfg_data_read */
        if (cur_entry == 0xffff) { // FW_CFG_INVALID
            return -1;
        }
        
        uint64_t value = 0;
        FWCfgEntry e = entries[cur_entry];
        uint32_t c_off = cur_offset + size - 1;
        cur_offset = c_off + 1;
        if (e.data && c_off < e.len) {
            while (size--) {
                value = (value << 8) | e.data[c_off--];
            }
        } 
        
        // uint64_t value = 0;
        // FWCfgEntry e = entries[cur_entry];
        // if (e.data && cur_offset < e.len) {
        //     do {
        //         printf("size: %d, cur_offset: %d\n", size, cur_offset);
        //         value = (value << 8) | e.data[cur_offset++];
        //     } while (--size && cur_offset < e.len);
        //
        //     value <<= 8 * size;
        // } 

        sel4utils_reg_set_val(regs, rd, value);

        // printf("fw_cfg_data_read, inst: 0x%x, selected_entry: 0x%x, size: 0x%x, data: 0x%lx\n", inst, cur_entry, size_saved, value);

        /* Write back CPU registers */
        int err = seL4_TCB_WriteRegisters(vm_get_vcpu_tcb(fault->vcpu), false, 0,
                                      sizeof(*regs) / sizeof(regs->pc), regs);
        assert(!err);
        if (err) {
            return err;
        }

    } else {
        return -1;
    }

    return 0;
}

int handle_page_fault(vm_t *vm, vm_vcpu_t *vcpu, fault_t *fault)
{
    int err;
    uintptr_t addr = fault_get_address(fault);
    size_t fault_size = fault_get_width_size(fault);

    if (addr >= 0x1e000000 && addr <= 0x1effffff) {
        // printf("vm access fw_cfg, addr = 0x%llx\, ip = 0x%llx, ecode = 0x%x\n", fault->addr, fault->ip, fault->fsr);
        err = handle_fw_cfg_access(fault);
        if (err) {
            print_fault(fault);
            abandon_fault(fault);
            return -1;
        }
        return ignore_fault(fault);
    }

    memory_fault_result_t fault_result = vm_memory_handle_fault(vm, vcpu, addr, fault_size);
    switch (fault_result) {
    case FAULT_HANDLED:
        return 0;
    case FAULT_RESTART:
        restart_fault(fault);
        return 0;
    case FAULT_IGNORE:
        return ignore_fault(fault);
    case FAULT_ERROR:
        print_fault(fault);
        abandon_fault(fault);
        return -1;
    case FAULT_UNHANDLED:
        if (vm->mem.unhandled_mem_fault_handler) {
            err = unhandled_memory_fault(vm, vcpu, fault);
            if (err) {
                return -1;
            }
            return 0;
        }
    default:
        break;
        /* We don't have a memory reservation for the faulting address
         * We move onto the rest of the page fault handler */
    }

    print_fault(fault);
    abandon_fault(fault);
    return -1;
}

int vm_guest_mem_abort_handler(vm_vcpu_t *vcpu)
{
    int err;
    fault_t *fault;
    fault = vcpu->vcpu_arch.fault;
    err = new_memory_fault(fault);
    if (err) {
        ZF_LOGE("Failed to initialise new fault");
        return -1;
    }
    err = handle_page_fault(vcpu->vm, vcpu, fault);
    if (err) {
        return VM_EXIT_HANDLE_ERROR;
    }
    return VM_EXIT_HANDLED;
}
