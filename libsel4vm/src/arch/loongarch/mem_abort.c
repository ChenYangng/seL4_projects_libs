static inline uint16_t swap_byte_16(uint16_t data)
{
  return (data << 8) | (data >> 8);
}

static inline uint32_t swap_byte_32(uint32_t data)
{
  uint16_t lower_bytes = swap_byte_16(data & 0xffff);
  uint16_t higher_bytes = swap_byte_16((data >> 16) & 0xffff);
  return (lower_bytes << 16) | (higher_bytes >> 16);
}

